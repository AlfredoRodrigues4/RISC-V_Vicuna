float my_exp(float expoente){

    float potencia = 1.0;
    float fatorial = 1.0;
    float result = 0.0;

    for(int i = 0; i < 30; i++){
        if(i == 0){
            potencia = 1.0;
            fatorial = 1.0;
        }
        else{
            potencia = potencia * expoente;
            fatorial = fatorial * i;
        }

        result = result + (potencia / fatorial);
    }
    return result;
   // printf("resultado Taylor: %lf \n\r", result);

}

